public class GPUChunkManager : MonoBehaviour
{
    [Header("Compute Shaders")]
    public ComputeShader caveGenerationShader;
    public ComputeShader meshGenerationShader;
    
    [Header("Settings")]
    public CaveSettings caveSettings;
    public Material caveMaterial;
    
    private ComputeBuffer densityBuffer;
    private ComputeBuffer chamberBuffer;
    private ComputeBuffer vertexBuffer;
    private ComputeBuffer triangleBuffer;
    
    private int generateDensityKernel;
    private int generateChamberKernel;
    private int generateTunnelKernel;

    private ComputeBuffer edgeTableBuffer;
    private ComputeBuffer triTableBuffer;
    private Vector3Int coordinate;

    private const int CHUNK_SIZE = Chunk.CHUNK_SIZE;
    private const float VOXEL_SIZE = Chunk.VOXEL_SIZE;
    
    public struct GPUChunkRequest
    {
        public Vector3Int coordinate;
        public int lodLevel;
        public System.Action<ChunkMeshData> onComplete;
    }
    
    public struct ChunkMeshData
    {
        public Vector3[] vertices;
        public int[] triangles;
        public Vector3[] normals;
        public Vector2[] uvs;
    }
    
    private Queue<GPUChunkRequest> requestQueue = new Queue<GPUChunkRequest>();
    private bool isProcessing = false;
    
    void Start()
    {
        InitializeComputeShaders();
        InitializeBuffers();
        InitializeMarchingCubesTables();
    }
    
    void InitializeComputeShaders()
    {
        if (caveGenerationShader == null)
        {
            Debug.LogError("Cave generation compute shader not assigned!");
            return;
        }
        
        // Find kernels
        generateDensityKernel = caveGenerationShader.FindKernel("GenerateDensityField");
        generateChamberKernel = caveGenerationShader.FindKernel("GenerateChamberField");
        generateTunnelKernel = caveGenerationShader.FindKernel("GenerateTunnelField");
    }
    
    void InitializeBuffers()
    {
        // Density field buffer
        int voxelCount = (CHUNK_SIZE + 1) * (CHUNK_SIZE + 1) * (CHUNK_SIZE + 1);
        densityBuffer = new ComputeBuffer(voxelCount, sizeof(float));
        
        // Vertex and triangle buffers for mesh generation
        int maxVertices = voxelCount * 4; // Conservative estimate
        int maxTriangles = voxelCount * 6;
        
        vertexBuffer = new ComputeBuffer(maxVertices, sizeof(float) * 3);
        triangleBuffer = new ComputeBuffer(maxTriangles, sizeof(int));
    }
    
    public void RequestChunk(GPUChunkRequest request)
    {
        requestQueue.Enqueue(request);
        
        if (!isProcessing)
        {
            ProcessNextRequest();
        }
    }
    
    void ProcessNextRequest()
    {
        if (requestQueue.Count == 0)
        {
            isProcessing = false;
            return;
        }
        
        isProcessing = true;
        var request = requestQueue.Dequeue();
        
        StartCoroutine(GenerateChunkGPU(request));
    }
    
    System.Collections.IEnumerator GenerateChunkGPU(GPUChunkRequest request)
    {
        // Set shader parameters
        Vector3 chunkOrigin = new Vector3(
            request.coordinate.x * CHUNK_SIZE * VOXEL_SIZE,
            request.coordinate.y * CHUNK_SIZE * VOXEL_SIZE,
            request.coordinate.z * CHUNK_SIZE * VOXEL_SIZE
        );
        
        caveGenerationShader.SetVector("ChunkOrigin", chunkOrigin);
        
        // Cave settings
        caveGenerationShader.SetVector("CaveSettings1", new Vector4(
            caveSettings.chamberFrequency,
            caveSettings.chamberMinRadius,
            caveSettings.chamberMaxRadius,
            caveSettings.chamberFloorFlatness
        ));
        
        caveGenerationShader.SetVector("CaveSettings2", new Vector4(
            caveSettings.tunnelMinRadius,
            caveSettings.tunnelMaxRadius,
            caveSettings.stratificationStrength,
            caveSettings.stratificationFrequency
        ));
        
        caveGenerationShader.SetVector("CaveSettings3", new Vector4(
            caveSettings.erosionStrength,
            caveSettings.rockHardness,
            caveSettings.minCaveHeight,
            caveSettings.maxCaveHeight
        ));
        
        caveGenerationShader.SetVector("NoiseSettings", new Vector4(
            caveSettings.seed,
            caveSettings.worleyOctaves,
            caveSettings.worleyPersistence,
            Time.time
        ));
        
        // Set buffers
        caveGenerationShader.SetBuffer(generateDensityKernel, "DensityField", densityBuffer);
        
        // Set chamber data if available
        var worldManager = GetComponent<WorldManager>();
        if (worldManager != null && worldManager.networkPreprocessor != null)
        {
            var chamberCenters = worldManager.networkPreprocessor.chamberCenters;
            if (chamberCenters.IsCreated && chamberCenters.Length > 0)
            {
                chamberBuffer = new ComputeBuffer(chamberCenters.Length, sizeof(float) * 3);
                chamberBuffer.SetData(chamberCenters.ToArray());
                
                caveGenerationShader.SetBuffer(generateDensityKernel, "ChamberCenters", chamberBuffer);
                caveGenerationShader.SetInt("ChamberCount", chamberCenters.Length);
            }
        }
        
        // Dispatch density generation
        int threadGroups = Mathf.CeilToInt((CHUNK_SIZE + 1) / 8.0f);
        caveGenerationShader.Dispatch(generateDensityKernel, threadGroups, threadGroups, threadGroups);
        
        // Wait for GPU
        yield return new WaitForEndOfFrame();
        
        // Read back density data
        float[] densityData = new float[(CHUNK_SIZE + 1) * (CHUNK_SIZE + 1) * (CHUNK_SIZE + 1)];
        densityBuffer.GetData(densityData);
        
        // Generate mesh on CPU for now (could be GPU accelerated)
        ChunkMeshData meshData = GenerateMeshFromDensity(densityData, request.lodLevel);
        
        // Callback with mesh data
        request.onComplete?.Invoke(meshData);
        
        // Clean up temporary buffer
        if (chamberBuffer != null)
        {
            chamberBuffer.Release();
            chamberBuffer = null;
        }
        
        // Process next request
        ProcessNextRequest();
    }
    ChunkMeshData GenerateMeshFromDensity(float[] densityData, int lodLevel)
    {
        if (meshGenerationShader == null)
        {
            UnityEngine.Debug.LogError("Mesh generation compute shader not assigned!");
            return new ChunkMeshData
            {
                vertices = new Vector3[0],
                triangles = new int[0],
                normals = new Vector3[0],
                uvs = new Vector2[0]
            };
        }

        // Initialize mesh generation buffers
        int voxelCount = CHUNK_SIZE * CHUNK_SIZE * CHUNK_SIZE;
        int maxVertices = voxelCount * 15; // Max 15 vertices per voxel (5 triangles)

        // Create buffers
        ComputeBuffer voxelOccupiedBuffer = new ComputeBuffer(voxelCount, sizeof(uint));
        ComputeBuffer voxelVertexCountBuffer = new ComputeBuffer(voxelCount, sizeof(uint));
        ComputeBuffer voxelVertexOffsetBuffer = new ComputeBuffer(voxelCount, sizeof(uint));
        ComputeBuffer meshVertexBuffer = new ComputeBuffer(maxVertices, sizeof(float) * 3);
        ComputeBuffer meshNormalBuffer = new ComputeBuffer(maxVertices, sizeof(float) * 3);
        ComputeBuffer meshTriangleBuffer = new ComputeBuffer(maxVertices, sizeof(int));
        ComputeBuffer counterBuffer = new ComputeBuffer(2, sizeof(uint)); // vertex count, triangle count

        // Copy density data to GPU buffer
        densityBuffer.SetData(densityData);

        // Find kernels
        int marchingCubesKernel = meshGenerationShader.FindKernel("MarchingCubes");
        int generateVerticesKernel = meshGenerationShader.FindKernel("GenerateVertices");

        // Set buffers for marching cubes kernel
        meshGenerationShader.SetBuffer(marchingCubesKernel, "DensityField", densityBuffer);
        meshGenerationShader.SetBuffer(marchingCubesKernel, "VoxelOccupied", voxelOccupiedBuffer);
        meshGenerationShader.SetBuffer(marchingCubesKernel, "VoxelVertexCount", voxelVertexCountBuffer);

        // Set edge and triangle tables (you'll need to create these as buffers)
        if (edgeTableBuffer == null) InitializeMarchingCubesTables();
        meshGenerationShader.SetBuffer(marchingCubesKernel, "EdgeTable", edgeTableBuffer);
        meshGenerationShader.SetBuffer(marchingCubesKernel, "TriTable", triTableBuffer);

        // Dispatch marching cubes
        int threadGroups = Mathf.CeilToInt(CHUNK_SIZE / 8.0f);
        meshGenerationShader.Dispatch(marchingCubesKernel, threadGroups, threadGroups, threadGroups);

        // Compute prefix sum for vertex offsets (simplified - in practice use a proper scan)
        uint[] vertexCounts = new uint[voxelCount];
        voxelVertexCountBuffer.GetData(vertexCounts);

        uint[] vertexOffsets = new uint[voxelCount];
        uint totalVertices = 0;
        for (int i = 0; i < voxelCount; i++)
        {
            vertexOffsets[i] = totalVertices;
            totalVertices += vertexCounts[i];
        }
        voxelVertexOffsetBuffer.SetData(vertexOffsets);

        // Set buffers for vertex generation
        meshGenerationShader.SetBuffer(generateVerticesKernel, "DensityField", densityBuffer);
        meshGenerationShader.SetBuffer(generateVerticesKernel, "VoxelOccupied", voxelOccupiedBuffer);
        meshGenerationShader.SetBuffer(generateVerticesKernel, "VoxelVertexOffset", voxelVertexOffsetBuffer);
        meshGenerationShader.SetBuffer(generateVerticesKernel, "Vertices", meshVertexBuffer);
        meshGenerationShader.SetBuffer(generateVerticesKernel, "Normals", meshNormalBuffer);
        meshGenerationShader.SetBuffer(generateVerticesKernel, "Triangles", meshTriangleBuffer);
        meshGenerationShader.SetBuffer(generateVerticesKernel, "EdgeTable", edgeTableBuffer);
        meshGenerationShader.SetBuffer(generateVerticesKernel, "TriTable", triTableBuffer);

        // Set chunk parameters
        meshGenerationShader.SetVector("ChunkOrigin", new Vector3(
            coordinate.x * CHUNK_SIZE * VOXEL_SIZE,
            coordinate.y * CHUNK_SIZE * VOXEL_SIZE,
            coordinate.z * CHUNK_SIZE * VOXEL_SIZE
        ));
        meshGenerationShader.SetInt("LODLevel", lodLevel);

        // Dispatch vertex generation
        meshGenerationShader.Dispatch(generateVerticesKernel, threadGroups, threadGroups, threadGroups);

        // Read back mesh data
        if (totalVertices > 0)
        {
            Vector3[] vertices = new Vector3[totalVertices];
            Vector3[] normals = new Vector3[totalVertices];
            int[] triangles = new int[totalVertices];

            meshVertexBuffer.GetData(vertices, 0, 0, (int)totalVertices);
            meshNormalBuffer.GetData(normals, 0, 0, (int)totalVertices);
            meshTriangleBuffer.GetData(triangles, 0, 0, (int)totalVertices);

            // Generate UVs (simple planar mapping for now)
            Vector2[] uvs = new Vector2[totalVertices];
            for (int i = 0; i < totalVertices; i++)
            {
                uvs[i] = new Vector2(vertices[i].x * 0.1f, vertices[i].z * 0.1f);
            }

            // Clean up buffers
            voxelOccupiedBuffer.Release();
            voxelVertexCountBuffer.Release();
            voxelVertexOffsetBuffer.Release();
            meshVertexBuffer.Release();
            meshNormalBuffer.Release();
            meshTriangleBuffer.Release();
            counterBuffer.Release();

            return new ChunkMeshData
            {
                vertices = vertices,
                triangles = triangles,
                normals = normals,
                uvs = uvs
            };
        }

        // Clean up buffers
        voxelOccupiedBuffer.Release();
        voxelVertexCountBuffer.Release();
        voxelVertexOffsetBuffer.Release();
        meshVertexBuffer.Release();
        meshNormalBuffer.Release();
        meshTriangleBuffer.Release();
        counterBuffer.Release();

        // Return empty mesh
        return new ChunkMeshData
        {
            vertices = new Vector3[0],
            triangles = new int[0],
            normals = new Vector3[0],
            uvs = new Vector2[0]
        };
    }

